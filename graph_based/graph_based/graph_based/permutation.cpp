Permutation::Permutation(int iN) : iN(iN), iOffset(0), iTotal(0) {
	assert(iN >= 1 && iN <= PERMUTATION_MAX);
	iTotal = 1;
	for (int i = 2; i <= iN; i++) {
		iTotal *= i;
	}
	iOffset = permutation_tab_size - iTotal;
	assert(iOffset >= 0);
}

vector<int> Permutation::operator[](int iIndex) {
	int *piData = &permutation_tab[offset() + iIndex][PERMUTATION_MAX - n()];
	return (vector<int>(piData, piData + n()));
}
